package day5_Thuchanh2;

public class Tinhluong {
	public double tinhLuong (double st1, double st2) 
	{
		return st1*st2;
	}
	
	public double tinhLuong (double st1, double st2, double st3) 
	{
		return st1*st2*st3;
	}
	
	public double tinhLuong (double st1, double st2, double st3, double st4) 
	{
		return st1*st2*st3*st4;
	}
}